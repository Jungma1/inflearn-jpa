        // 위의 코드를 아래와 같이 변경할 수 있음
        // 변경 감지를 사용하면 원하는 속성만 선택해서 변경할 수 있음 (merge 는 모든 속성이 변경됨)
        // 실무에서는 위의 코드보다 아래의 코드를 더 많이 사용함
